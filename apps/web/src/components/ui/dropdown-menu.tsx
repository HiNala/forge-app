function DropdownMenuSubTrigger({
  className,
  inset,
  children,
  ...props
}: React.ComponentProps<typeof DropdownMenuPrimitive.SubTrigger> & {
  inset?: boolean;
}) {
  return (
    <DropdownMenuPrimitive.SubTrigger
      data-slot="dropdown-menu-sub-trigger"
      className={cn(
        "flex cursor-pointer select-none items-center rounded-md px-2 py-2 text-sm outline-none",
        "font-body font-medium text-text",
        "focus:bg-accent-light data-[state=open]:bg-accent-light",
        inset && "pl-8",
        className,
      )}
      {...props}
    >
      {children}
      <ChevronRight className="ml-auto size-4 opacity-70" />
    </DropdownMenuPrimitive.SubTrigger>
  );
}

